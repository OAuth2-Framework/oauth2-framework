namespace OAuth2Framework\Bundle\Server\ScopeManagerPlugin\DependencyInjection\Compiler;

use Symfony\Component\DependencyInjection\Compiler\CompilerPassInterface;
use Symfony\Component\DependencyInjection\ContainerBuilder;
use Symfony\Component\DependencyInjection\Reference;

class AuthorizationCodeGrantTypeCompilerPass implements CompilerPassInterface
{
    /**
     * {@inheritdoc}
     */
    public function process(ContainerBuilder $container)
    {
        if (!$container->hasDefinition('oauth2_server.auth_code.grant_type')) {
            return;
        }

        $definition = $container->getDefinition('oauth2_server.auth_code.grant_type');
        $definition->addMethodCall('enableScopeSupport', [new Reference('oauth2_server.scope_manager')]);
    }
}
